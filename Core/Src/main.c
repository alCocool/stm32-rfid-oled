void I2C_Scanner() {
    // On scanne de l'adresse 1 à 127
    for (uint16_t i = 1; i < 128; i++) {
        // Le STM32 attend l'adresse décalée de 1 bit vers la gauche (<< 1)
        if (HAL_I2C_IsDeviceReady(&hi2c1, (uint16_t)(i << 1), 3, 5) == HAL_OK) {
            mon_adresse_ecran = i; // BINGO ! On sauvegarde l'adresse
            break; // On arrête de chercher, on a trouvé l'écran
        }
    }
}
